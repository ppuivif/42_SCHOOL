#include <unistd.h>

void	ft_putchar(int nb)
{
	char	num;

	num = nb + '0';
	write(1, &num, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
		write (1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			nb *= -1;
			write(1, "-", 1);
		}
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
		}
		nb %= 10;
		ft_putchar(nb);
	}
}
