#include <unistd.h>

void	ft_rot13(void)
{
	char alpha;

	alpha = 'a';
	while (alpha <= ('a' + 13))
	{
		alpha = alpha + 13;
		write(1, &alpha, 1);
	}
}

int main(void)
{
	ft_rot13();
	return (0);
}
